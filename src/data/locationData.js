// Helper function to get all cities in a flat array
export const getAllCities = () => {
  const cities = [];
  
  Object.entries(locationData).forEach(([continent, countries]) => {
    Object.entries(countries).forEach(([country, states]) => {
      Object.entries(states).forEach(([state, citiesArray]) => {
        citiesArray.forEach(city => {
          cities.push({
            ...city,
            state,
            country,
            continent,
            fullLocation: `${city.name}, ${state}, ${country}`
          });
        });
      });
    });
  });
  
  return cities.sort((a, b) => a.name.localeCompare(b.name));
};

// Helper function to search cities
export const searchCities = (query) => {
  const allCities = getAllCities();
  const searchTerm = query.toLowerCase();
  
  return allCities.filter(city => 
    city.name.toLowerCase().includes(searchTerm) ||
    city.state.toLowerCase().includes(searchTerm) ||
    city.country.toLowerCase().includes(searchTerm) ||
    city.fullLocation.toLowerCase().includes(searchTerm)
  ).slice(0, 20); // Limit to 20 results
};

// Calculate distance between two coordinates (Haversine formula)
export const calculateDistance = (lat1, lng1, lat2, lng2) => {
  const R = 6371; // Earth's radius in kilometers
  const dLat = (lat2 - lat1) * Math.PI / 180;
  const dLng = (lng2 - lng1) * Math.PI / 180;
  const a = Math.sin(dLat/2) * Math.sin(dLat/2) +
            Math.cos(lat1 * Math.PI / 180) * Math.cos(lat2 * Math.PI / 180) *
            Math.sin(dLng/2) * Math.sin(dLng/2);
  const c = 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1-a));
  return R * c; // Distance in kilometers
};